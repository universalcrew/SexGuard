use sex\guard\Manager;

use pocketmine\Player;
use pocketmine\item\Item;


/**
 * @todo nothing.
 */
class ListArgument extends Manager
{
	/**
	 * @var Manager
	 */
	private $api;


	/**
	 * @param Manager $api
	 */
	function __construct( Manager $api )
	{
		$this->api = $api;
	}


	/**
	 *                                          _
	 *   __ _ _ ____ _ _   _ _ __ _   ___ _ ___| |_
	 *  / _' | '_/ _' | | | | '  ' \ / _ \ '_ \   _\
	 * | (_) | || (_) | |_| | || || |  __/ | | | |_
	 *  \__,_|_| \__, |\___/|_||_||_|\___|_| |_|\__\
	 *           /___/
	 *
	 * @param  Player   $sender
	 * @param  string[] $args
	 *
	 * @return bool
	 */
	function execute( Player $sender, array $args ): bool
	{
		$api  = $this->api;
		$list = $api->getRegionList($sender->getName());

		if( count($list) < 1 )
		{
			$sender->sendMessage($api->getValue('list_empty'));
			return TRUE;
		}

		$name = [];

		foreach( $list as $region )
		{
			$name[] = $region->getRegionName();
		}

		$message = $api->getValue('list_success');
		$message = str_replace('{list}', implode(', ', $name), $message);

		$sender->sendMessage($message);
		return TRUE;
	}
}